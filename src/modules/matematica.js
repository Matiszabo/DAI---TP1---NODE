// Constante PI
const PI = 3.14;

// Función suma
function sumar(x, y) {
    return x + y;
}

// Función multiplicar (arrow function)
const multiplicar = (a, b) => {
    return a * b;
};

// Array de strings
const strings = ["dos", "cuatro", "ocho", "diez"];

// Exportación de las constantes, funciones y el array
export { PI, sumar, multiplicar, strings };
